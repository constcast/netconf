    private Set selectedDevices = new HashSet();
    /**
     * Getter for property selectedDevice.
     * @return Value of property selectedDevice.
     */
    public boolean isSelectedDevice() {
        TableRowDataProvider rowData = (TableRowDataProvider) getBean("currentRow");
        if(rowData == null) return false;
        RowKey rowId= rowData.getTableRow();
        if(selectedDevices.contains(rowId)){
            return true;
        }
        else{
            return false;
        }
    }

    /**
     * Setter for property selectedDevice.
     * @param selectedDevice New value of property selectedDevice.
     */
    public void setSelectedDevice(boolean selectedDevice) {
        TableRowDataProvider rowData = (TableRowDataProvider) getBean("currentRow");
        if(rowData != null){
            String rowId = rowData.getTableRow().getRowId();
            if(selectedDevice){
                this.selectedDevices.add(rowId);
            } else {
                this.selectedDevices.remove(rowId);
            }
        }
    }

    /*
     * Validator checking if the name is already in use.
     */ 
    public void name_validate(FacesContext context, UIComponent component, Object value) {
        String newScenarioName = String.valueOf(value);
        
        List scenarioList = getApplicationBean1().getAppScenarioDataProvider().scenarioList;
        Iterator listIterator = scenarioList.iterator();
        ArrayList nameList = new ArrayList(scenarioList.size());
        while(listIterator.hasNext()){
        Scenario currentScenario = (Scenario) listIterator.next();
        nameList.add(currentScenario.getName());
        }
        if(nameList.contains(newScenarioName)) throw new ValidatorException(new FacesMessage
		("Scenario name is already used! Please choose a different name."));
        
        
    }


    public String button1_action() {
        List newDeviceList = new ArrayList();
        Iterator devicesIterator = selectedDevices.iterator();
        while(devicesIterator.hasNext()){
            String rowId = (String) devicesIterator.next();
            RowKey rowKey = getApplicationBean1().getAppDeviceDataProvider().getRowKey(rowId);
            Device currentDevice = (Device) getApplicationBean1().getAppDeviceDataProvider().getObject(rowKey);
            newDeviceList.add(currentDevice.getName());
        }
        this.scenario.setDeviceList(newDeviceList);
        getApplicationBean1().getAppScenarioDataProvider().addObject(this.scenario);
        getApplicationBean1().getAppScenarioDataProvider().commitChanges();
        
        List scenarioList = getApplicationBean1().getAppScenarioDataProvider().getList();
        
        try{
            getApplicationBean1().getAppScenarioStorage().saveList(scenarioList);
        }
        catch(Exception ex){
            error("Error writing to file!");
            log("Error writing to file!", ex);
        }
        
        return "saveScenario";
    }

